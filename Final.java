public class Final {
    private String resultado;

    public Final() {
    }

    public Final(String resultado) {
        this.resultado = resultado;
    }

    public String getResultado() {
        return resultado;
    }

    public void setResultado(String resultado) {
        this.resultado = resultado;
    }

    @Override
    public String toString() {
        return "Final [resultado=" + resultado + "]";
    }
    
    
}
